#Faça um algoritmo que calcule a área de um terreno. Os terrenos são retangulares, e deverão ser informadas as medidas de largura e comprimento do terreno.

b = int (input ("Informe o comprimento do terreno: "))
h = int (input ("Informe a largura do terreno: "))
 

A = b * h #calculo da área do terreno

print ("A medida de largura o terreno é: ", h )
print ("A medida de comprimento do terreno é: ", b )
print ("A área do terreno é: ", A )